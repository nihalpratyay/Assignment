package interihence;

public class Occurence {

	public static void main(String[] args) {
		String str = "Hello, World";
		
		System.out.println("First occurence of o is: "+ str.indexOf('o'));
		System.out.println("Last ocurrence of o is: "+ str.lastIndexOf('o'));
		System.out.println("First ocurrence of ',' is: "+ str.indexOf(','));
		

	}

}
